package com.workdaycal;

public class TotalWorklog {
	private String projectid;
	private String projname;
	private String sum_total_work;
	private String sumworked;
	private String batch;
	private String createddate;
	
	public String getProjectid() {
		return projectid;
	}
	public void setProjectid(String projectid) {
		this.projectid = projectid;
	}
	public String getProjname() {
		return projname;
	}
	public void setProjname(String projname) {
		this.projname = projname;
	}
	public String getSum_total_work() {
		return sum_total_work;
	}
	public void setSum_total_work(String sum_total_work) {
		this.sum_total_work = sum_total_work;
	}
	public String getSumworked() {
		return sumworked;
	}
	public void setSumworked(String sumworked) {
		this.sumworked = sumworked;
	}
	public String getBatch() {
		return batch;
	}
	public void setBatch(String batch) {
		this.batch = batch;
	}
	public String getCreateddate() {
		return createddate;
	}
	public void setCreateddate(String createddate) {
		this.createddate = createddate;
	}
	
}
